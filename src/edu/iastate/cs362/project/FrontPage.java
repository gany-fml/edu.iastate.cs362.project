    private void buttonAccountMouseClicked(java.awt.event.MouseEvent evt) {                                           
    	Main.controller.createAccount(loggedUser.getUsername());   
    	loggedAccounts = loggedUser.getAccounts();
    	String accountID = loggedAccounts.get(loggedAccounts.size()-1).getAccountID();
    	AccountTab tab = new AccountTab(accountID, this);
    	tabs.add(tab);
        tabbedPane.addTab(accountID, tab);
    }                                          

    private void buttonPNumMouseClicked(java.awt.event.MouseEvent evt) {                                        
    	String num = (String)JOptionPane.showInputDialog(
                "Update Phone Number (Must be 10 digits):");
    	if(num != null && num.length() == 10)
    	{
    		Main.controller.updatePhoneNumber(loggedUser.getUsername(), num);    	
    		pNumber.setText(Main.controller.getPhoneNumber(loggedUser.getUsername()));
    	} else {
    		JOptionPane.showMessageDialog(null, "Phonenumber length must be 10 digits");
    	}
    }                                       

    private void buttonTransferMouseClicked(java.awt.event.MouseEvent evt) {                                            
    	String acc1 = (String)JOptionPane.showInputDialog(
                "Enter account to transfer FROM:");
    	String acc2 = (String)JOptionPane.showInputDialog(
                "Enter account to transfer TO:");
    	String amount = (String)JOptionPane.showInputDialog(
                "Enter amount:");
    	if(acc1 != null && acc2 != null && amount != null)
    	{
    		Main.controller.transferMoney(loggedUser.getUsername() + "-" + acc1, 
    				loggedUser.getUsername() + "-" + acc2, Double.valueOf(amount));
    		
    		///Update the text for each text for balances
    		for(AccountTab tab : tabs)
    		{
    			tab.updateBalance();
    		}
    	}
    }                                           

    private void buttonLoanReqMouseClicked(java.awt.event.MouseEvent evt) {                                           
    	String amount = (String)JOptionPane.showInputDialog(
                "Loan amount:");
    	if(amount != null)
    	{
    		Main.controller.requestLoan(loggedUser.getUsername(), Double.valueOf(amount));
    	}
    }                                          

    private void buttonPassMouseClicked(java.awt.event.MouseEvent evt) {                                        
    	String pass = (String)JOptionPane.showInputDialog(
                "Change password: ");
    	if(pass != null)
    	{
	    	if(!Main.controller.changePassword(loggedUser.getUsername(), pass))
	    	{
	    		JOptionPane.showMessageDialog(this,
	    			    "Password change failed.",
	    			    "Error!",
	    			    JOptionPane.ERROR_MESSAGE);
	    	}	    		
    	}
    }                                       

    private void buttonFixedDepMouseClicked(java.awt.event.MouseEvent evt) {                                            
    	String accountID = (String)JOptionPane.showInputDialog(
                "Account id: ");
    	String amount = (String)JOptionPane.showInputDialog(
                "Amount: ");
    	String months = (String)JOptionPane.showInputDialog(
                "Months: ");
    	if(accountID != null && amount != null && months != null)
    	{
    		Main.controller.fixedDeposit(loggedUser.getUsername() + "-" + accountID, Double.valueOf(amount), Integer.valueOf(months));
    		
    		///Update the text for each text for balances
    		for(AccountTab tab : tabs)
    		{
    			tab.updateBalance();
    			updateTotal();
    		}
    	}
    }                                           

