    public void updateTotal()
    {   	
    	totalBalance.setText("$" + new DecimalFormat("#0.00").format(loggedUser.getTotalBalance()));
    }
    
    public void deletePage(String id, String toID)
    {
    	for(int i = 0; i < tabs.size(); ++i)
    	{
    		if(tabs.get(i).id == id)
    		{
    			tabs.get(i).updateBalance();
    			updateTotal();
    			tabbedPane.remove(i+1); // Account for the home tab
    			tabs.remove(i);
    		}
    		if(tabs.get(i).id == toID)
    		{
    			tabs.get(i).updateBalance();
    		}
    	}
    }
    
